static uint16_t CountNameList(const char *names[])
{
	uint16_t i;

	i = 0;
	while(names[i] != NULL)
		i += 1;

	return i;
}



char* CreatePlayerName(void)
{
	uint16_t fname_count = CountNameList(firstname);
	uint16_t lname_count = CountNameList(lastname);
	char buffer[MAX_NAME_LEN + 4];

	// 15% chance of a single name
	if(chance(15))
		snprintf(buffer, MAX_NAME_LEN, "%s", firstname[xrnd(fname_count)]);
	else
		snprintf(buffer, MAX_NAME_LEN, "%s %s", firstname[xrnd(fname_count)], lastname[xrnd(lname_count)]);

	return strdup(buffer);
}
